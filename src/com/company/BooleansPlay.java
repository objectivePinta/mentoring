package com.company;

public class BooleansPlay {

    public static void main(String[] args) {
        boolean ninge = true;
        boolean ploua = false;
        boolean eSoare = false;

        System.out.println(ninge && ploua);
        System.out.println(ninge || ploua);
        System.out.println(ninge && !ploua);
        System.out.println(ninge || !eSoare);


    }
}
